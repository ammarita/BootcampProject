package com.example.BootcampProject;

import java.util.Date;

public class JsonObjectFromDB {
	public int id;
	public Date date;
	public int Steps;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public Date getDate() {
		return date;
	}
	public void setDate(Date date) {
		this.date = date;
	}
	public int getSteps() {
		return Steps;
	}
	public void setSteps(int steps) {
		Steps = steps;
	}
	

}
